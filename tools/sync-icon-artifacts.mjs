import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const ICONS_TS_PATH = resolve("src", "icons.ts");
const ICON_CATALOG_PATH = resolve("icon", "icon-pack-catalog.json");
const ICON_INVENTORY_PATH = resolve("docs", "emoji-inventory.md");
const OPENMOJI_SVG_DIR_PATH = resolve("icon", "openmoji", "svg");

const readImportedOpenmojiTokens = () => {
  const files = readdirSync(OPENMOJI_SVG_DIR_PATH)
    .filter((name) => name.toLowerCase().endsWith(".svg"))
    .sort((a, b) => a.localeCompare(b));

  return files.map((name) => `asset:openmoji:${name.slice(0, -4)}`);
};

const readEmojiPacksFromSource = () => {
  const source = readFileSync(ICONS_TS_PATH, "utf8");
  const match = source.match(
    /export const EMOJI_PACKS:\s*readonly\s+EmojiPackDefinition\[\]\s*=\s*(\[[\s\S]*?\])\s*as const;/u,
  );

  if (match === null || match[1] === undefined) {
    throw new Error("[MEMORYBLOX] Could not locate EMOJI_PACKS in src/icons.ts.");
  }

  const arrayLiteral = match[1];
  const packs = Function(`"use strict"; return (${arrayLiteral});`)();

  if (!Array.isArray(packs)) {
    throw new TypeError("[MEMORYBLOX] Parsed EMOJI_PACKS is not an array.");
  }

  return packs;
};

const formatIconRows = (icons) => {
  const rows = [];
  const maxLineLength = 78;
  let currentLine = "";

  for (const icon of icons) {
    const nextLine = currentLine.length === 0 ? icon : `${currentLine} ${icon}`;

    if (nextLine.length > maxLineLength) {
      if (currentLine.length > 0) {
        rows.push(currentLine);
      }
      currentLine = icon;
      continue;
    }

    currentLine = nextLine;
  }

  if (currentLine.length > 0) {
    rows.push(currentLine);
  }

  return rows.join("\n");
};

const buildInventoryMarkdown = (packs, importedOpenmojiTokens) => {
  const date = new Date().toISOString().slice(0, 10);
  const activeAssetTokens = [...new Set(
    packs.flatMap((pack) => pack.icons).filter((icon) => icon.startsWith("asset:")),
  )].sort((a, b) => a.localeCompare(b));
  const importedTokenSet = new Set(importedOpenmojiTokens);
  const activeImportedTokens = activeAssetTokens.filter((token) => importedTokenSet.has(token));
  const inactiveImportedTokens = importedOpenmojiTokens.filter(
    (token) => !activeImportedTokens.includes(token),
  );

  const lines = [
    "# Icon Inventory",
    "",
    "_Generated from `src/icons.ts` by `tools/sync-icon-artifacts.mjs`._",
    "_Do not edit this file manually._",
    "",
    `## Latest Update (${date})`,
    "",
    "- Inventory regenerated from the canonical icon source in `src/icons.ts`.",
    `- Pack count: ${packs.length} (must remain even for the 2-column Settings layout).`,
    "- Every pack enforces unique icons with a minimum size policy (50+).",
    `- Imported OpenMoji SVG tokens on disk: ${importedOpenmojiTokens.length}.`,
    `- Active imported SVG tokens currently used in packs: ${activeImportedTokens.length}.`,
    "",
    "## Packs Overview",
    "",
  ];

  packs.forEach((pack, index) => {
    lines.push(`${index + 1}. **${pack.name}** (\`${pack.id}\`)`);
  });

  lines.push("");

  for (const pack of packs) {
    lines.push(`## ${pack.name}`);
    lines.push("");
    lines.push(formatIconRows(pack.icons));
    lines.push("");
  }

  lines.push("## Imported SVG Library (OpenMoji)");
  lines.push("");
  lines.push("### Active Imported Tokens");
  lines.push("");
  lines.push(activeImportedTokens.length > 0 ? formatIconRows(activeImportedTokens) : "_None_");
  lines.push("");
  lines.push("### Inactive Imported Tokens");
  lines.push("");
  lines.push(inactiveImportedTokens.length > 0 ? formatIconRows(inactiveImportedTokens) : "_None_");
  lines.push("");

  lines.push("## Tuning Notes");
  lines.push("");
  lines.push("- Keep each pack at 50+ unique icons for 1x no-repetition safety.");
  lines.push("- Keep the total pack count even to preserve the 2-column Settings layout.");
  lines.push("- Re-run `npm run icons:sync` after any pack changes.");
  lines.push("");

  return `${lines.join("\n")}`;
};

const main = () => {
  const packs = readEmojiPacksFromSource();
  const importedOpenmojiTokens = readImportedOpenmojiTokens();

  writeFileSync(ICON_CATALOG_PATH, `${JSON.stringify(packs, null, 2)}\n`, "utf8");
  writeFileSync(
    ICON_INVENTORY_PATH,
    buildInventoryMarkdown(packs, importedOpenmojiTokens),
    "utf8",
  );

  console.log(`[MEMORYBLOX] Synced ${packs.length} icon packs to ${ICON_CATALOG_PATH}.`);
  console.log(`[MEMORYBLOX] Synced icon inventory to ${ICON_INVENTORY_PATH}.`);
};

main();
